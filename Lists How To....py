#Appending A list
numbers1=[1,2,3,4,5]
print(numbers1)
numbers1.append(6)
print(numbers1, "\n")

#Inserting an element
numbers2=[12,24,48,60,72]
print(numbers2)
numbers2.insert(2,36)
print(numbers2, "\n")

#Sorting a list
numbers3=[25, 78, 12, 43, 88]
print(numbers3)
numbers3.sort()
print(numbers3, "\n")

